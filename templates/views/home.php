<p>I am very pleased to take up the role of Chairman of the IAMTA and to welcome you to our website. This website serves to inform all our members and the broader public of the work we do and I invite you to explore these web pages to learn more about our association.
</p>
<p>The IAMTA always welcomes new members and we provide and share teaching resources for new and experienced teachers of applied maths.
</p>
<p>Every year we organise two major events for students, a senior applied maths quiz and a junior problem solving competition. National finals for both of these events takes place in Portlaoise Education Centre every year.
</p>
<p>We also organise an annual conference for teachers at different venues across the country which includes a range of different workshops to help assist teachers of applied maths. Special awards are also presented to the top 3 students in the country in applied Maths based on their leaving cert result and to a teacher for their outstanding achievements in applied maths. I would like to take this opportunity to thank the sponsors of the awards, namely the Raymond Kearns Foundation, Folens Publishers and the Institute of Numerical and Computational Analysis (INCA).
</p>
The IAMTA Committee consists of teachers who voluntarily give their time to ensure this organisation continues to grow, without their commitment, hard work and dedication the organisation would cease to exist. Please feel free to contact any committee member if you have any queries.
